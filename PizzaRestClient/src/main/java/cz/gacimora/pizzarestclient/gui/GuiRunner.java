    private void UpdateCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateCustomerActionPerformed
        CustomerClient restClient = new CustomerClient();
        String id = this.jTextField1_CustomerId.getText();

        try {
            Customer existingCustomer = restClient.find_JSON(Customer.class, id);
            existingCustomer.setName(this.jTextField1_FirstName.getText());
            existingCustomer.setSurname(this.jTextField1_Surname.getText());
            existingCustomer.getMyOrders().get(0).getOrderedPizzas().get(0).setName(this.jTextField1_PizzaName.getText());
            existingCustomer.getMyOrders().get(0).getOrderedPizzas().get(0).setPrice(this.jTextField1_PizzaPrice.getText());
            restClient.edit_JSON(existingCustomer, id);
            ClearFormValues();
            this.jTextArea1_output.setText("Customer record updated successfully");
        } catch (Exception e) {
            this.jTextArea1_output.setText("Customer record update failed with exception: " + e.toString());
        }
    }//GEN-LAST:event_UpdateCustomerActionPerformed

